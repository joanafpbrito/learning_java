package Ex_06;

public class Athlete {
    private String name;
    private String sport;
    private double height;
    private double weight;
    private String origin;

    public Athlete(String name, String sport, double height, double weight, String origin) {
        this.name = name;
        this.sport = sport;
        this.height = height;
        this.weight = weight;
        this.origin = origin;
    }


}
