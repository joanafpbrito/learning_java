package Ex_03;

public class Animal {
    private String name;
    private String specie;
    private String origin;
    private double weight;
    private String [] food;

    public Animal(String name, String specie, String origin, double weight, String[] food) {
        this.name = name;
        this.specie = specie;
        this.origin = origin;
        this.weight = weight;
        this.food = food;
    }


}
